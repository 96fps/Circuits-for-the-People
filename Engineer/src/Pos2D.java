
public class Pos2D {
    private int x;
    private int y;

    public Pos2D() {
        this(0, 0);
    }

    public Pos2D(int posX, int posY) {
        x = posX;
        y = posY;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
